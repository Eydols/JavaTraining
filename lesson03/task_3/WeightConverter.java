package task_3;

public class WeightConverter {

    public static double convertKgToGr(double x) {
        return x * 1000;
    }

    public static double convertKgToMg(double x) {
        return x * 1000000;
    }

    public static double convertKgToTon(double x) {
        return x / 1000;
    }
}


 